package class07HW;

public class homework3 {
    public static void main(String[] args) {
        int counter = 20;
        while (counter >= 1) {
            System.out.print(counter + " ");
            counter -= 2;
        }
        counter = 20;
        System.out.println();
        for (counter = 20; counter >= 1; counter -= 2) {
            System.out.print(counter + " ");
        }
    }
}

