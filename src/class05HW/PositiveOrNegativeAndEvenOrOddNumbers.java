package class05HW;

public class PositiveOrNegativeAndEvenOrOddNumbers {
    public static void main(String[] args) {
        int number = -3;
        if (number > 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        }
        int price = 18;
        if (price == price/2*2){
            System.out.println("Even number");
        }else{
            System.out.println("Odd number");

        }
    }
}
